""""
Syntax for lamda:
lambda arguments: expression

lambda: The keyword that introduces a lambda function.
arguments: One or more arguments for the lambda function, separated by commas.
expression: The operation or calculation that the lambda function performs.

"""

# Example 1:
# For Multiplication
x = lambda a, b, c: a * b * c
print(x(5, 4, 3))  # 60

# Example 2:
# For Addition
x = lambda a, b, c: a + b + c
print(x(12, 40, 8))
